#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	size_t		i;

	d = (char *)dest;
	s = (const char *)src;
	i = 0;
	if (dest != src)
	{
		while (i < n)
		{
			d[i] = (char)s[i];
			i++;
		}
	}
	return (dest);
}
