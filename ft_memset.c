#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*v;
	size_t	i;

	i = 0;
	v = (char *)s;
	while (i < n)
	{
		v[i] = (char)c;
		i++;
	}
	return ((void *)v);
}
