int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	r;

	i = 0;
	j = 1;
	r = 0;
	if (str)
	{
		while ((str[i] >= 9 && str[i] <= 13) || str[i] == '+' || str[i] == '-'
			|| str[i] == 32)
		{
			if (str[i] == '-')
				j = (-1);
			i++;
		}
		while (str[i] >= 48 && str[i] <= 57)
		{
			r = (r * 10) + str[i] - 48;
			i++;
		}
		return (r * j);
	}
	else
		return (0);
}
