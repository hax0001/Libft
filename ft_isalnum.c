#include <stdlib.h>
#include <stdio.h>

int	ft_isalnum(int i)
{
	if (i >= 0 && i <= 9 || i >= 65 && i <= 90
		|| i >= 97 && i <= 122)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
